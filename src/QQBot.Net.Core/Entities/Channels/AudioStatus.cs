namespace QQBot;

/// <summary>
///     表示音频状态。
/// </summary>
internal enum AudioStatus
{
    /// <summary>
    ///     开始播放操作。
    /// </summary>
    Start = 0,

    /// <summary>
    ///     暂停播放操作。
    /// </summary>
    Pause = 1,

    /// <summary>
    ///     继续播放操作。
    /// </summary>
    Resume = 2,

    /// <summary>
    ///     停止播放操作。
    /// </summary>
    Stop = 3
}
