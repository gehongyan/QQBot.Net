namespace QQBot;

/// <summary>
///     表示一个富文本段落的对齐方式。
/// </summary>
public enum TextAlignment
{
    /// <summary>
    ///     表示左对齐。
    /// </summary>
    Left = 0,

    /// <summary>
    ///     表示居中对齐。
    /// </summary>
    Middle = 1,

    /// <summary>
    ///     表示右对齐。
    /// </summary>
    Right = 2
}
